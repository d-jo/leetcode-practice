package sudoku_test

import (
	"testing"

	sudoku "github.com/d-jo/leetcode/valid_sudoku"
)

func TestSudoku1(t *testing.T) {
	input := [][]byte{
		{'8', '3', '.', '.', '7', '.', '.', '.', '.'},
		{'6', '.', '.', '1', '9', '5', '.', '.', '.'},
		{'.', '9', '8', '.', '.', '.', '.', '6', '.'},
		{'8', '.', '.', '.', '6', '.', '.', '.', '3'},
		{'4', '.', '.', '8', '.', '3', '.', '.', '1'},
		{'7', '.', '.', '.', '2', '.', '.', '.', '6'},
		{'.', '6', '.', '.', '.', '.', '2', '8', '.'},
		{'.', '.', '.', '4', '1', '9', '.', '.', '5'},
		{'.', '.', '.', '.', '8', '.', '.', '7', '9'},
	}

	expected := false

	actual := sudoku.IsValidSudoku(input)

	if actual != expected {
		t.Errorf("expected %t gt %t", expected, actual)
	}
}

